package orientObj02.calculadora;

public class Calculadora {
	
	static public int somar(int a, int b) {
		return a + b;
	}
	
	static public int subtrair(int a, int b) {
		return a - b;
	}
	
	static public int multiplicar(int a, int b) {
		return a * b;
	}
	
	static public int dividir(int a, int b) {
		return a / b;
	}
	
	static public int elevarPotencia(int a, int b) {
		return (int) Math.pow(a, b);
	}

}
